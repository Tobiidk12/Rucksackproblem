public class Weights {

    private int weight;
    private String name;
    private int value;

public Weights(int weight_c, String name_c, int value_c) {
    weight = weight_c;
    name = name_c;
    value = value_c;
}

public int getWeight () {
    return weight;
}
public String getName () {
    return name;
}
public int getValue () {
    return value;
}


}
